    /**
     *
     * @return String JSON com informaçoes de um endereço através do envio de um
     * cep.
     * @throws MalformedURLException
     * @throws IOException
     */
    private String buscarCep() throws MalformedURLException, IOException {
        String cep = txCep.getText();
        String request = "http://viacep.com.br/ws/" + cep + "/json/";
        URL url = new URL(request);
        HttpURLConnection con = (HttpURLConnection) url.openConnection();
        con.setRequestMethod("GET");
        con.setReadTimeout(10000);
        con.setConnectTimeout(15000);
        con.connect();
        InputStream inputStream = con.getInputStream();
        String jSonResposta = new Scanner(inputStream, "UTF-8").useDelimiter("\\ A").next();
        return jSonResposta;

    }

