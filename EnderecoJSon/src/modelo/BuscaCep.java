    /**
     * Função que cria um objeto a partir de uma String JSON e exibe os dados na
     * tela
     *
     * @param evt
     */
    private void btObjetoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btObjetoActionPerformed
        try {
            String obj = this.buscarCep();
            JSONObject json = new org.json.JSONObject(obj);
            String logradouro = json.getString("logradouro");
            String bairro = json.getString("bairro");
            String cidade = json.getString("localidade");
            String estado = json.getString("uf");
            String cep = json.getString("cep");
            Endereco end = new Endereco(logradouro, bairro, cidade, estado, cep);
            txaSaida.setText(end.toString());
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (JSONException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_btObjetoActionPerformed

